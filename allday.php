                <h3>Getting here</h3>
                <h4>From Bristol city centre</h4>
                <p>From Bristol Bus Station, take the <a href="https://www.firstgroup.com/uploads/maps/Service_46_X46_WEB.pdf">46 bus</a> (destination Yate) to Frenchay. It stops at the junction of Bristol Rd and Beckspool Rd, a short walk from the venue.</p>

                <h4>By train</h4>
                <p>Bristol Parkway may be the most convenient station for this singing. Booking in advance is strongly recommended for cheaper tickets. From Parkway, the <a href="https://www.firstgroup.com/uploads/maps/Bristol_Service_18_18A_WEB.pdf">18/18A</a> bus (destination Emersons Green) runs to Bristol Rd, a short walk from the venue.
                </p>

                <h4>By coach</h4>
                <p>National Express services run to Bristol Bus Station. Megabus services run to Bond Street, near Cabot Circus. Follow local directions from the city centre as above.</p>

                <h4>By car</h4>
                <p>Leave the M4 at junction 19 onto the M32. Take the first exit at junction 1 onto the A4174, then the first right
                onto Bristol Rd and then the first left onto Beckspool Rd. There is on-street parking on Beckspool Rd outside the venue.</p>

