                <h3>Getting here</h3>
                <h4>By train</h4>
                <p>Book a train to <strong>Bristol Temple Meads</strong> (not Bristol Parkway).
                Booking in advance is strongly recommended for cheaper tickets. There is a connecting service to 
                <strong>Shirehampton</strong> station, which is a ten minute walk from the venue. 
                <a href="http://traintimes.org.uk/bristol+temple+meads/shirehampton/10:30a/2016-05-07">Check timetable</a> &mdash; 
                we recommend the 09:16 service towards Severn Beach.</p>
                
                <h4>By coach</h4> 
                <p>National Express services run to Bristol Bus Station. Megabus services run to Bond Street, near Cabot Circus.
                Take the 40 or 41 bus from Broadmead Union Street to Shirehampton Green. <a href="http://goo.gl/maps/HnGCT">[map]</a></p>
    
                <h4>By car</h4>
                <p>Leave the M5 at junction 18. There is on-street parking on Station Rd outside the venue.</p>
                
                
